		<p class="text-justify">
			Looking at Year-wise values of female to male authored paper ratio, AGR and MED disciplines show
			significant growth in the female to male authored papers ratio during the last 10 years.AGR starts
			with a value of 0.38 in 2008 which rises to 0.54 in 2017. MED starts with a value of 0.41 in 2008 which
			rises to 0.55 in 2017. BIO discipline also shows growth towards the recent period, with a value of 0.66
			in the most recent year (perhaps the time when higher output has been recorded).
			<br>
			Disciplines like GEO (0.33 to 0.39), INF (0.19 to 0.26), MAT (0.24 to 0.32) show very low rise in the
			ratio. Some other disciplines like MAR (0.36 to 0.38), SS (0.42 to 0.47) and ENG (0.24 to 0.28)
			show almost no or negligible growth in the female to male authored paper ratio. Thus, while some
			disciplines seem to be becoming more attractive for women researchers, few others continue to remain
			unappealing for women researchers.
