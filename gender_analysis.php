		<p class="text-justify">
			A higher ratio of women in Higher Education and research is a very important development goal in many
			countries across the world. There are many countries which provides special initiatives and schemes to
			increase participation of women in education. The 2030 agenda for
			<a href="https://sustainabledevelopment.un.org/post2015/transformingourworld" target="_blank">Sustainable
				Development</a>
			by United Nations lists seventeen development goals, which includes -</p>
		<ul>
			<li><b>Goal 4</b>: Ensure inclusive and equitable quality education and promote lifelong learning
				oppurtunities for all.
			</li>
			<li><b>Goal 5</b>: Achieve gender equality and empower all women girls.</li>
		</ul>
		<p class="text-justify">
			But even after so much efforts, according to UNESCO Institute for Statistics
			<a href="http://uis.unesco.org/sites/default/files/documents/fs51-women-in-science-2018-en.pdf"
			   target="_blank">
				fact sheet</a>
			2018, women account for less than 30% of the total researchers across the world. India has taken several
			initiatives to enhance enrollment of women in higher education and research. According to data from
			University Grants Commission of India, women accounted for about 48% of the total enrolment in higher
			education institutions in the year 2016-17. Out of all Ph.D.s awarded in 2015-16, women account for
			about 39% degrees awarded. According to a report by Department of Science and Technology of India,
			as on 2015 there are about 14% women out of total full-time personnel engaged in R&D activities in the
			country. Similarly, women participation in extramural research projects supported by central and
			state government funding bodies has been about 29% as in 2014-153.
			<br>
			Here, a case study was made to characterize the participation of women in research, by analysing
			the parameters of institution-type, discipline, citation impact and international collaboration.
			Research publication data from 50 most productive Indian institutions, along with data for 5 major
			institution systems, for a period of 10 years (2008–2017), as indexed in Web of Science, is obtained
			as sample data and analysed.
